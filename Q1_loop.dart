void main() {
  print("\nFor Loop\n");
  for (int i = 0; i < 5; i++) {
    print("HELLO SUNWAY");
  }
  print("\nWHile Loop\n");
  int i=0;
  while (i < 5) {
    print("HELLO SUNWAY");
    i++;
  }
  print("\nDo While Loop\n");
  int loop = 0;
  do {
    print("HELLO SUNWAY");
    loop++;
  } while (loop < 3);
}
